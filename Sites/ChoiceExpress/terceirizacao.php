    <a name="terc01"><h3>O que n&atilde;o se deve Teceirizar</h3></a>
    <p></p>
    <p>A atividade-fim de uma empresa &eacute; a raz&atilde;o de existir dessa empresa. Dentro do servi&ccedil;o p&uacute;blico, t&ecirc;m-se exemplos de terceiriza&ccedil;&otilde;es satisfat&oacute;rias, como &eacute; o caso dos servi&ccedil;os de limpeza. Neste caso, a terceiriza&ccedil;&atilde;o &eacute; indicada pois a atividade-fim do servi&ccedil;o p&uacute;blico n&atilde;o &eacute; a limpeza de seu patrim&ocirc;nio.</p>
	<p>Para amenizar os problemas causados pela terceiriza&ccedil;&atilde;o, est&atilde;o sendo criadas por empresas de software algumas solu&ccedil;&otilde;es de sistemas informatizados para promover de forma mais eficaz o controle e a gest&atilde;o da m&atilde;o-de-obra terceirizada, sendo uma importante ferramenta para o departamento de recursos humanos das empresas que praticam a terceiriza&ccedil;&atilde;o.</p>
    <a name="terc02"><h3>Teceirizados</h3></a>
    <p>Apesar das v&aacute;rias vantagens, a Terceiriza&ccedil;&atilde;o deve ser praticada com cautela. Uma m&aacute; gest&atilde;o de terceiriza&ccedil;&atilde;o pode implicar para as empresas um descontrole e desconhecimento de sua m&atilde;o-de-obra, a contrata&ccedil;&atilde;o involunt&aacute;ria de pessoas inadequadas, perdas financeiras em a&ccedil;&otilde;es trabalhistas movidas pelos empregados terceirizados, dentre outros problemas.</p>
    <a name="terc03"><h3>O processo de terceirização em uma organização</h3></a>
    <p>O processo de terceirização em uma organização deve levar em conta diversos fatores de interesse, tais como a redução de custos e principalmente o foco na sua atividade-fim. Há um sério risco em atrelar a terceirização à redução de custo, porque, na maioria das vezes, não é esse o resultado. A terceirização precisa estar em conformidade com os objetivos estratégicos da organização, os quais irão revelar em que pontos ela poderá alcançar resultados satisfatórios.</p>

