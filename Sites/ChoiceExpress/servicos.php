    <a name="serv01"><h3>Portaria</h3></a>
    <p></p>
    <p>Executa atividades relacionadas ao zelo e à fiscalização da propriedade. O cargo de porteiro é de muita responsabilidade e exige habilidade e sutileza para lidar com o ser humano e estar atento a tudo o que acontece ao 		     seu redor.</p>
	<p>Suas funções são determinadas pelo síndico, mas em geral sua tarefa é:
	Vigiar as portas (entrada e saída) do estabelecimento;
	Proibir a entrada de vendedores, pedintes e compradores;</p>
	<p>
    Permanecer na portaria, uniformizado, não abandonar seu posto sem que haja outro porteiro para substituir, sendo atencioso;
    atender prontamente o interfone;
    entregar correspondências;
	controlar a iluminação da portaria e dos jardins, conforme instruções do zelador;
	ajudar os moradores do edifício na carga e descarga de volumes;
	ceder informações;
	encaminhar visitantes ao local pretendido;
	alertar o zelador sobre qualquer pessoa cuja entrada ou permanência no edifício dê margem a dúvida ou suspeita de más intenções;
	observar e fazer cumprir todos os regulamentos do edifício;
	substituir o zelador caso necessário;</p>
    
    <a name="serv02"><h3>Recepção</h3></a>
    <p>Recepcionar, controlar e encaminhar visitantes ao local ou pessoa desejada;
	Responder perguntas gerais sobre a empresa ou direcioná-las para quem as possa responder;</p>
    <p>Enviar e receber, organizar e distribuir ao destinatário correspondências e/ou produtos;
	arquivar documentos;
	axecutar trabalhos de digitação;
	Controlar agendas, marcar compromissos (como viagens e reuniões);</p>
    <p>Manter atualizado os livros de registros de correspondência e registro de fax;
	efetuar telefonemas, atender telefone e transferir chamadas telefônicas;
	anotar recados de visitantes ou de chamadas telefônicas e enviá-los para os funcionários;
	atuar com ética no exercício da função: imagem profissional, imagem da empresa, sigilo profissional, relacionamento com colegas e superiores.</p>

    <a name="serv03"><h3>Zelador</h3></a>
    <p>Como o próprio nome diz, zela pela segurança das pessoas e do patrimônio.</p>
    <p>Dentre as suas funções estão:
	fazer cumprir os regulamentos de edifício e determinações do Síndico e administradora;
	contato direto com a administração, auxiliando nas contas;
	fiscalizar os empregados do edifício;
	recomendar advertências e outras sanções aos empregados que não estiverem cumprindo a contento suas funções;
	responsabilidade pelo dia-a-dia do local de trabalho;</p>
    <p>Realização de pequenos reparos;
	condução de elevadores;
	controle da movimentação, tanto de pessoas quanto de veículos no estacionamento
	receber mercadorias;
	receber as solicitações para uso do salão de festas e encaminhá-las à administradora, vistoriando, anterior e posteriormente.;
	somente permitir a entrada de carga e mudança para o prédio, pelo elevador de serviço;
	comandar o abandono do prédio em situações de emergência, chamando bombeiros, polícia, pronto-socorro, etc., se necessário.</p>

